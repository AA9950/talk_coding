# Create two sets
set1 = {1, 2, 3, 4}
set2 = {3, 4, 5, 6}

# Calculate union
union = set1 | set2
print("Union:", union)

# Calculate intersection
intersection = set1 & set2
print("Intersection:", intersection)

# Calculate difference
difference = set1 - set2
print("Difference:", difference)

